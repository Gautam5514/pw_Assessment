// Write a C++ program to find size of basic data types.

#include <iostream>
using namespace std;
int main() {
    cout << "Int sizeof(int) " << sizeof(int) << endl;
    cout << "Char sizeof(char) " << sizeof(char) << endl;
    cout << "Double sizeof(double) " << sizeof(double) << endl;
    cout << "float sizeof(float) " << sizeof(float) << endl;
    cout << "long sizeof(long) " << sizeof(long) << endl;
    cout << "short sizeof(short) " << sizeof(short) << endl;
    cout << "long double sizeof(long Double) " << sizeof(long double) << endl;
    cout << "bool sizeof(bool) " << sizeof(bool) <<endl;
    return 0;
}